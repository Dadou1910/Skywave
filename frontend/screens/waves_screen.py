    def __init__(self, engine: DataEngine, parent=None):
        super().__init__(parent)
        self._engine  = engine
        self._history: dict[str, deque] = {
            key: deque(maxlen=HISTORY_LEN) for key, *_ in WAVE_BANDS
        }
        self._curves: dict[str, pg.PlotDataItem] = {}
        self._meters: dict[str, _BandMeter]      = {}
        self._build_ui()
        self._engine.new_scores.connect(self._on_scores, Qt.ConnectionType.QueuedConnection)

    def _build_ui(self) -> None:
        # Outer layout holds a scroll area so the docs section is reachable
        outer = QVBoxLayout(self)
