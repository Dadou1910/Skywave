# ── Band meter (custom-painted animated equaliser bar) ─────────────────────────

class _BandMeter(QWidget):

    METER_W = 56
    METER_H = 130
    LABEL_H = 38

    def __init__(self, label: str, freq: str, color: str, parent=None):
        super().__init__(parent)
        self.setFixedSize(self.METER_W, self.METER_H + self.LABEL_H + 20)
        self._label = label
        self._freq  = freq
        self._color = QColor(color)
        self._value = 0.0

    def set_value(self, v: float) -> None:
        self._value = max(0.0, min(100.0, v))
        self.update()

    def paintEvent(self, _event) -> None:
        p = QPainter(self)
        p.setRenderHint(QPainter.RenderHint.Antialiasing)

        W, BAR_X, BAR_W = self.width(), 8, self.width() - 16
        BAR_T, BAR_H, R  = 20, self.METER_H, 5

        p.setPen(Qt.PenStyle.NoPen)
        p.setBrush(QColor("#1a1a22"))
        p.drawRoundedRect(BAR_X, BAR_T, BAR_W, BAR_H, R, R)

        fill_h = max(2, int(BAR_H * self._value / 100))
        fill_y = BAR_T + BAR_H - fill_h
        col_top, col_bot = QColor(self._color), QColor(self._color)
        col_top.setAlphaF(1.0)
        col_bot.setAlphaF(0.25)
        grad = QLinearGradient(0, fill_y, 0, fill_y + fill_h)
        grad.setColorAt(0.0, col_top)
        grad.setColorAt(1.0, col_bot)
        p.setBrush(grad)
        p.drawRoundedRect(BAR_X, fill_y, BAR_W, fill_h, R, R)

        if self._value > 2:
            glow = QColor(self._color)
            glow.setAlphaF(0.9)
            p.setPen(QPen(glow, 2))
            p.drawLine(BAR_X + 3, fill_y + 1, BAR_X + BAR_W - 3, fill_y + 1)

        f_val = QFont("Inter"); f_val.setPixelSize(10); f_val.setBold(True)
        p.setFont(f_val)
        p.setPen(self._color)
        p.drawText(0, 2, W, 16,
                   Qt.AlignmentFlag.AlignHCenter | Qt.AlignmentFlag.AlignTop,
                   f"{self._value:.1f}%")

        label_y = BAR_T + BAR_H + 6
        f_lbl = QFont("Inter"); f_lbl.setPixelSize(10); f_lbl.setBold(True)
        p.setFont(f_lbl)
        p.setPen(QColor(Colors.TEXT_PRIMARY))
        p.drawText(0, label_y, W, 16,
                   Qt.AlignmentFlag.AlignHCenter | Qt.AlignmentFlag.AlignTop,
                   self._label)

        f_freq = QFont("Inter"); f_freq.setPixelSize(9)
        p.setFont(f_freq)
        p.setPen(QColor(Colors.TEXT_MUTED))
        p.drawText(0, label_y + 18, W, 14,
                   Qt.AlignmentFlag.AlignHCenter | Qt.AlignmentFlag.AlignTop,
                   self._freq)
        p.end()


