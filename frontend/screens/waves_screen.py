    def _on_scores(self, pkt) -> None:
        bands = pkt.raw_bands
        if not bands:
            return
        total  = sum(bands.values()) or 1
        normed = {key: bands.get(key, 0) / total * 100 for key, *_ in WAVE_BANDS}

        if self._notice.isVisible():
            self._notice.setVisible(False)

        for key, pct in normed.items():
            if key in self._meters:
                self._meters[key].set_value(pct)
            self._history[key].append(pct)

        n   = len(next(iter(self._history.values())))
        pad = HISTORY_LEN - n
        xs  = list(range(HISTORY_LEN))
        for key in normed:
            ys = list(self._history[key]) + [math.nan] * pad
            curve = self._curves.get(key)
            if curve is not None:
                try:
                    curve.setData(xs, ys)
                except RuntimeError:
                    pass
